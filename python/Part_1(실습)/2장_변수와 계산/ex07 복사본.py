from math import * # math 라이브러리의 모든 함수를 사용할 수 있게끔 한다.

#절대값 구하기
print(abs(-777))
print(abs(777))

#반올림하기
print(round(1.2222))
print(round(1.6222))

#주어진 매개변수 값 중에서 최댓값 구하기
print(max(10,-20,100,1000))
#주어진 매개변수 값 중에서 최소값 구하기
print(min(10,-20,100,1000))

#4.0의 제곱근 구하기
print(sqrt(4.0))
#3의 2승을 구하기 (연산자 **와 동일한 함수)
print(pow(3,2))