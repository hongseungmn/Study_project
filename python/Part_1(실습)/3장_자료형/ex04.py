#문자열에 대한 실습


welcome = "Happy New 'Year' 2021"  
# 문자열 안에 " or '를 넣기 위해서는 문자열을 선언 할 때 사용했던 따옴표와 다른 것을 사용한다.
print(welcome)

# \뒤의 문자는 무시한다고 생각한다.
message = 'dosen\'t'
print(message)
message = "\"Yes,\" I can do it"
print(message)

#문자열의 길이를 알고자 한다면 len()함수를 이용한다.
message = "홍성민"
print("문자열의 길이 : ", len(message))
